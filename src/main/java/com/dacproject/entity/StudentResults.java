package com.dacproject.entity;

public class StudentResults {
int student_id;
int assign_marks;
int lab_marks;
int total;
String subject_id;
public int getStudent_id() {
	return student_id;
}
public void setStudent_id(int student_id) {
	this.student_id = student_id;
}
public int getAssign_marks() {
	return assign_marks;
}
public void setAssign_marks(int assign_marks) {
	this.assign_marks = assign_marks;
}
public int getLab_marks() {
	return lab_marks;
}
public void setLab_marks(int lab_marks) {
	this.lab_marks = lab_marks;
}
public int getTotal() {
	return total;
}
public void setTotal(int total) {
	this.total = total;
}
public String getSubject_id() {
	return subject_id;
}
public void setSubject_id(String subject_id) {
	this.subject_id = subject_id;
}


}
