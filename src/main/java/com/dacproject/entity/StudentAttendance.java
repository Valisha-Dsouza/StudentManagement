package com.dacproject.entity;

public class StudentAttendance {
int student_id;
int total_class;
int class_present;
int subject_id;
public int getStudent_id() {
	return student_id;
}
public void setStudent_id(int student_id) {
	this.student_id = student_id;
}
public int getTotal_class() {
	return total_class;
}
public void setTotal_class(int total_class) {
	this.total_class = total_class;
}
public int getClass_present() {
	return class_present;
}
public void setClass_present(int class_present) {
	this.class_present = class_present;
}
public int getSubject_id() {
	return subject_id;
}
public void setSubject_id(int subject_id) {
	this.subject_id = subject_id;
}

}
